# summarize.py — STEP3: 카드뉴스용 요약 생성
#
# [지시사항]
# 1. 핵심 주제 한 문장 요약
# 2. 포인트 3~4개, 각 40자 이내
# 3. 전문 용어는 쉬운 표현으로
# 4. 과장/자극적 표현 지양, 사실 기반
# 5. 출처(채널명/영상제목/URL) 별도 표기
# =============================================

import json

from groq_client import 한국어_완성


def 카드뉴스_요약(정리된_스크립트, 영상제목, 채널명, url):
    system_prompt = "당신은 정리된 유튜브 스크립트를 카드뉴스 제작용으로 요약하는 에디터입니다."
    user_prompt = f"""아래 정리된 스크립트를 바탕으로 카드뉴스용 요약을 작성하세요.

영상 제목: {영상제목}
채널명: {채널명}
URL: {url}

[정리된 스크립트]
{정리된_스크립트[:8000]}

[지시사항]
1. 영상의 핵심 주제를 두 문장으로 요약하세요 (배경 한 문장 + 핵심 사실 한 문장). 너무 뭉뚱그리지 말고 구체적인 숫자·이름·수치를 포함하세요.
2. 핵심 내용을 5개의 포인트로 나누어 작성하세요. 각 포인트는 **주어와 서술어를 갖춘 완전한 문장**이어야 하고, 60자 이내로 작성하세요.
   - 절대로 "251억 청구서", "카드 결제 거절" 같은 명사구만 나열하지 마세요. 무슨 일이 있었는지 읽으면 바로 이해되게 쓰세요.
   - 스크립트에 나오는 구체적인 숫자·날짜·발언·기관명을 최대한 포인트에 녹여서 정보량을 높이세요.
   - 5개 포인트는 서로 다른 새로운 사실을 담아야 합니다 (원인/경과/피해자 반응/기업 반응/전망 등으로 나누면 좋습니다). summary와 포인트끼리 내용이 겹치면 안 됩니다.
   - 스크립트에서 5개의 구분되는 사실을 찾기 어려우면, 배경 설명이나 영향/전망을 추가해 5개를 채우되 지어내지는 마세요.
3. 전문 용어는 쉬운 표현으로 풀어쓰세요.
4. 과장되거나 자극적인 표현은 지양하고, 사실 기반으로 작성하세요.
5. 카드뉴스 표지용 제목은 20자 이내, 후킹 문구는 30자 이내로 작성하세요.

[좋은 예시 / 나쁜 예시]
나쁨: "251억 청구서" (무슨 일인지 알 수 없는 명사구)
좋음: "이용자는 자동화 스크립트 오류로 하루 만에 251억원이 청구됐다고 밝혔다"
나쁨: "카드 결제 거절"
좋음: "카드사는 이상 결제로 판단해 승인을 거절했지만 일부 금액은 이미 빠져나갔다"

[출력 형식]
반드시 아래 JSON 형식으로만 응답하세요. 다른 설명은 붙이지 마세요.
{{
  "cover_title": "표지용 제목 (20자 이내)",
  "hook": "후킹 문구 (30자 이내)",
  "summary": "핵심 요약 두 문장 (배경+핵심 사실, 구체적 숫자 포함)",
  "points": ["완전한 문장 형태의 포인트1 (60자 이내)", "포인트2", "포인트3", "포인트4", "포인트5"],
  "comment": "포인트4·5에 대한 한 줄 코멘트, 완전한 문장 (사실 요약 톤, 45자 이내)"
}}
"""

    응답 = 한국어_완성(system_prompt, user_prompt, max_tokens=900, max_tokens_fallback=700)
    if 응답 is None:
        return None

    try:
        json_시작 = 응답.index("{")
        json_끝 = 응답.rindex("}") + 1
        결과 = json.loads(응답[json_시작:json_끝])
    except (ValueError, json.JSONDecodeError) as e:
        print(f"  [경고] 요약 JSON 파싱 실패: {e}\n원본 응답: {응답}")
        return None

    포인트 = 결과.get("points", [])
    while len(포인트) < 5:
        포인트.append("")
    결과["points"] = 포인트[:5]

    결과["source_channel"] = 채널명
    결과["source_title"] = 영상제목
    결과["source_url"] = url

    return 결과


if __name__ == "__main__":
    샘플 = 카드뉴스_요약(
        "오픈AI가 새로운 이미지 생성 모델을 발표했습니다. 기존보다 훨씬 빠르고 사실적인 이미지를 만들 수 있습니다.",
        "오픈AI 신모델 발표", "테크채널", "https://youtube.com/watch?v=abc"
    )
    print(json.dumps(샘플, ensure_ascii=False, indent=2))
